package com.ciji.pojo;

public class User {
	private int user_id;
	private String user_email;
	private String user_password;
	private String user_name;
	private int user_age;
	private String user_regester_time;
	private String user_logintime;
	private String user_headShot;
	private int count;
	
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getUser_password() {
		return user_password;
	}
	public void setUser_password(String user_password) {
		this.user_password = user_password;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public int getUser_age() {
		return user_age;
	}
	public void setUser_age(int user_age) {
		this.user_age = user_age;
	}
	public String getUser_headShot() {
		return user_headShot;
	}
	public void setUser_headShot(String user_headShot) {
		this.user_headShot = user_headShot;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getUser_regester_time() {
		return user_regester_time;
	}
	public void setUser_regester_time(String user_regester_time) {
		this.user_regester_time = user_regester_time;
	}
	public String getUser_logintime() {
		return user_logintime;
	}
	public void setUser_logintime(String user_logintime) {
		this.user_logintime = user_logintime;
	}
	
	
}
